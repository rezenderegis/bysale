package com.sale.modelo;

import java.util.Date;

public class VendaCliente {

	private Long idvendacliente;

	private String idcliente;

	private Date dtvenda;

	private int situacaovenda;

	private Double totalVenda;

	private Double descontoVenda;

	public Long getIdvendacliente() {
		return idvendacliente;
	}

	public void setIdvendacliente(Long idvendacliente) {
		this.idvendacliente = idvendacliente;
	}


	public String getIdcliente() {
		return idcliente;
	}

	public void setIdcliente(String idcliente) {
		this.idcliente = idcliente;
	}

	public Date getDtvenda() {
		return dtvenda;
	}

	public void setDtvenda(Date dtvenda) {
		this.dtvenda = dtvenda;
	}

	public int getSituacaovenda() {
		return situacaovenda;
	}

	public void setSituacaovenda(int situacaovenda) {
		this.situacaovenda = situacaovenda;
	}

	public Double getTotalVenda() {
		return totalVenda;
	}

	public void setTotalVenda(Double totalVenda) {
		this.totalVenda = totalVenda;
	}

	public Double getDescontoVenda() {
		return descontoVenda;
	}

	public void setDescontoVenda(Double descontoVenda) {
		this.descontoVenda = descontoVenda;
	}

}
